package Intro;

public class ComparisonOperator {

	public static void main(String[] args) {
		int a = 6;
		int b= 7;
		boolean c= a==b;
		boolean d= a != b;
		boolean e= a <= b;
		boolean f = a > b;
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);

	}

}
